                <div class="about-page__tabs-contents-two" id="history-content">
                    <h3>In 1959 Ian Venus and Brian Hartung set up their first office in a small room at the back of
                        383 Flinders Lane in Melbourne.</h3>
                    <div class="about-page__tabs-contents-two-section">
                        <h3>1960</h3>
                        <p> A year later, when Australian import restrictions were lifted, Ian and Brian looked beyond
                            Australia’s shores for supplies. They soon introduced new products such as jute twine from
                            Scotland,
                            brown kraft paper from Austria and water-activated tape from England to the Australian
                            market.</p>
                    </div>
                    <div class="about-page__tabs-contents-two-section">
                        <h3>1962</h3>
                        <p>In 1962 the partnership dissolved. Ian Venus retained Venus Hartung & Company while Brian
                            Hartung
                            established a new business and retained the non-packaging agencies.</p>
                    </div>
                    <div class="about-page__tabs-contents-two-section">
                        <h3>1962</h3>
                        <p>In 1962 the partnership dissolved. Ian Venus retained Venus Hartung & Company while Brian
                            Hartung established a new business and retained the non-packaging agencies.</p>
                        <p> Later in 1962, Venus Hartung won its first major contract – to supply the Australian
                            government with
                            cellulose tape imported from Austria.</p>
                    </div>
                    <div class="about-page__tabs-contents-two-section">
                        <h3>1963</h3>
                        <p>In 1963, Venus Hartung’s first sales manager, Peter Rush, introduced a new concept in
                            packaging tape
                            – self adhesive kraft paper that was also silicone coated. Following this, in 1964, Venus
                            introduced a self adhesive plastic PVC tape. Another Australian first that totally changed
                            the direction of carton sealing in Australia.</p>
                    </div>
                    <div class="about-page__tabs-contents-two-section">
                        <h3>1965</h3>
                        <p> By 1965 Venus Hartung had outgrown the 2 floors it occupied in Flinders Lane and built a new
                            warehouse in Abbotsford, an inner Melbourne suburb, and in 1969 the company incorporated to
                            become Venus Hartung Pty Ltd.</p>
                    </div>
                    <div class="about-page__tabs-contents-two-section">
                        <h3>1970's</h3>
                        <p>Throughout the early 70’s, under the guidance of Sales Director, Frank Trchala, Venus Hartung
                            enjoyed enormous growth in the sales of packaging machinery, heat sealers for polythene
                            bags,
                            filling and unitising equipment, shrink wrap, skin packaging, carton sealing and strapping
                            machines,
                            and shrink and stretch wrappers for loaded pallets.</p>
                    </div>
                    <div class="about-page__tabs-contents-two-section">
                        <h3>1976</h3>
                        <p>In 1976 the company diversified into ropes and twines for packaging, mining, fishing,
                            transport and
                            agriculture. Joe Schirrippa, Sales Manager from 1976 to 1993, led these developments,
                            particularly
                            in the area of harvesting twines and by 1985 the company occupied two city blocks with a
                            warehousing
                            and administrative complex that covered 100,000 square feet.</p>
                    </div>
                    <div class="about-page__tabs-content-two-section">
                        <h3>1994</h3>
                        <p>The appointment of Wayne Shearer as Marketing Manager in 1994 began an era when Venus
                            classified its
                            products into 15 distinct categories: Poly Bags, Films, Heat Sealers, Self Adhesive Tapes,
                            Tape
                            Dispensers and Carton Sealers, Strapping, Strapping Machines and Tools, Pallet Wrappers,
                            Netting
                            Products, Hotmelt Glues and Glueguns, Agriculture and Harvesting Twines, Warehouse Equipment
                            and
                            Materials Handling, Rubber Bands ad Ties, Ropes and Twines, Carton Stapling and Net
                            Clipping.</p>
                    </div>
                    <div class="about-page__tabs-contents-two-section">
                        <h3>1990's</h3>
                        <p>The appointment of Wayne Shearer as Marketing Manager in 1994 began an era when Venus
                            classified its
                            products into 15 distinct categories: Poly Bags, Films, Heat Sealers, Self Adhesive Tapes,
                            Tape
                            Dispensers and Carton Sealers, Strapping, Strapping Machines and Tools, Pallet Wrappers,
                            Netting
                            Products, Hotmelt Glues and Glueguns, Agriculture and Harvesting Twines, Warehouse Equipment
                            and
                            Materials Handling, Rubber Bands ad Ties, Ropes and Twines, Carton Stapling and Net
                            Clipping.</p>
                        <p>
                            Increasing sophistication in the range of heat sealing, band sealing, laminating, vacuum
                            sealers,
                            skin and shrink packaging equipment continued throughout the 1990s, making Venus Australia’s
                            market
                            leader in heat sealing.</p>
                        <p> In the 1990’s Venus expanded the range of harvesting products – in particular by introducing
                            big
                            bale twines in 1992 and HDPE netwrap for big round bales in 1993. When Phil Pomeroy joined
                            the sales
                            team in 1995 Venus added more big bale twines, then 3 layer pit covers and Venus stretch
                            silage
                            film.</p>
                    </div>
                    <div class="about-page__tabs-contents-two-section">
                        <h3>1997</h3>
                        <p> Venus purchased the current property at 555 Church Street Richmond in 1997. The
                            multi-million dollar
                            project involved extensive re-building and opened in 1999. The 4 level complex covers a city
                            block
                            and has 3 container bays.</p>
                    </div>
                    <div class="about-page__tabs-contents-two-section">
                        <h3>2006</h3>
                        <p> The company appointed Tim Venus as Operations Director in 2006. Tim brought with him
                            extensive
                            experience in marketing and sales.</p>
                    </div>
                    <div class="about-page__tabs-contents-two-section">
